/**
 * Represents a point in a two-dimensional space.
 * @class Point
 */
class Point {
  /**
   * Creates a Point object with given x and y coordinates.
   * @constructor
   * @param {number} x - The x-coordinate of the point.
   * @param {number} y - The y-coordinate of the point.
   */
  constructor(x, y) {
    this.x = x;
    this.y = y;
  }
}
